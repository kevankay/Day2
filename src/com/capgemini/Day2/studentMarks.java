package com.capgemini.Day2;

public class studentMarks {

	public static double calStudentSum(double maths, double physics, double english) {
		   double sum = maths + physics + english;
		return sum;
	}

	public static double calStudentAverage(double maths, double physics, double english) {
		   double sum = maths + physics + english;
		   double Average = sum/3;
		return Average;
	}

	public static double calSubjectSum(int shiny, int swathi, int keerth) {
		   int sum = shiny + swathi + keerth;
		return sum;
	}

	public static double calSubjectAverage(int shiny, int swathi, int keerth) {
		   int sum = shiny + swathi + keerth;
		   int Average = sum /3;
		return Average;
	}

}
